    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try (Connection connection = getConnection();) {
            // Sets new database image
            ImageIcon imageIcon = new ImageIcon("database.connected.png"); 
            Image image = imageIcon.getImage(); 
            Image newimg = image.getScaledInstance(128, 128, Image.SCALE_SMOOTH); 
            imageIcon = new ImageIcon(newimg);  
            jLabel5.setIcon(imageIcon);
            
            // Saves properties in a file for future use 
            // For use only in case of frequent database ons and offs
            new PrintWriter(new OutputStreamWriter(new FileOutputStream(
                "database.properties.txt"), "UTF-8"), true);
            System.out.print(jTextField1.getText() + " ");
            System.out.print(jTextField2.getText() + " ");
            System.out.print("BookShop ");
            System.out.print(jTextField3.getText() + " "); 
            System.out.println(jPasswordField1.getSelectedText() + " ");
                    
            // Shows modal dialog window
            JOptionPane.showMessageDialog(this, "Database connection has "
                + "succeeded!", "" + connection.getMetaData().
                getDatabaseProductName() + " " + connection.getMetaData().
                getDatabaseProductVersion(), JOptionPane.INFORMATION_MESSAGE);
            
            // Hides panel
            this.setVisible(false);
        } catch (SQLException ex) {
            // Sets new database image
            ImageIcon imageIcon = new ImageIcon("database.notConnected.png"); 
            Image image = imageIcon.getImage(); 
            Image newimg = image.getScaledInstance(128, 128, Image.SCALE_SMOOTH); 
            imageIcon = new ImageIcon(newimg);  
            jLabel5.setIcon(imageIcon);
            
            // Hides modal dialog window
            JOptionPane.showMessageDialog(this, "Database connection has "
                + "failed!", "Connection Error", JOptionPane.ERROR_MESSAGE);
            
            // Sets primary display
            this.setVisible(true);
            initDisplay();
            Logger.getLogger(DatabaseLogin.class.getName()).log(Level.
                SEVERE, null, ex);    
        } catch (FileNotFoundException | UnsupportedEncodingException ex) {
            // Sets primary display
            this.setVisible(true);
            initDisplay();
            Logger.getLogger(DatabaseLogin.class.getName()).log(Level.
                SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


