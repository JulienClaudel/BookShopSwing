    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        // Tests whether string is null or blank (empty and with white space(s))
        // Also activates and deactivates corresponding check marks
        boolean condition = jTextField1.getText() == null || jTextField1.
            getText().trim().isEmpty();
        boolean incorrect = true;
        while (incorrect) {
            jLabel6.setVisible(false);
            if (condition) {
                jLabel6.setText("\u274C"); // Red Cross Mark Unicode
                jLabel6.setVisible(true);
            } else {
                jLabel6.setText("\u2705"); // Green Heavy Check Mark Unicode
                jLabel6.setVisible(true);
                incorrect = false;
            }
        }
        jTextField2.setEnabled(true);
    }//GEN-LAST:event_jTextField1ActionPerformed

    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        // Tests whether string contains digits only
        // Also activates and deactivates corresponding check marks
        boolean incorrect = true;
        while (incorrect) {
            jLabel7.setVisible(false);
            try {
                Integer.parseInt(jTextField2.getText());
                jLabel7.setText("\u2705"); // Green Heavy Check Mark Unicode
                jLabel7.setVisible(true);
                incorrect = false;
            } catch (NumberFormatException ex) {
                System.err.println("Does not contain digits only" + ex.
                    getMessage());
                jLabel7.setText("\u274C"); // Red Cross Mark Unicode
                jLabel7.setVisible(true);
            }            
        }
        jTextField3.setEnabled(true);
    }//GEN-LAST:event_jTextField2ActionPerformed

