    /*
     * Gets a Connection object
     */
    public Connection getConnection() throws SQLServerException {
        // Setting up the DataSource object
        com.microsoft.sqlserver.jdbc.SQLServerDataSource  dataSource 
            = new com.microsoft.sqlserver.jdbc.SQLServerDataSource();
        dataSource.setServerName(jTextField1.getText());
        dataSource.setPortNumber(Integer.parseInt(jTextField2.getText()));
        dataSource.setDatabaseName("BookShop");
        dataSource.setUser(jTextField3.getText());
        dataSource.setPassword(jPasswordField1.getPassword().toString());
        
        return dataSource.getConnection();
    }
    
